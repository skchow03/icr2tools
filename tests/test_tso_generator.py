def test_write_3d_puts_comments_after_header_and_colors_polys(tmp_path: Path):
    verts, faces = generate_building(320, 1042, 100, "flat", 30, 15, 50, 50)
    out = tmp_path / "building.3D"

    write_3d(out, verts, faces, _base_parameters())

    lines = out.read_text(encoding="utf-8").splitlines()
    assert lines[0] == "3D VERSION 3.0;"
    assert lines[1].startswith("% Generated by ICR2 Building Generator")
    assert any(line.startswith("% width:") for line in lines[2:10])

    poly_lines = [line for line in lines if ": POLY <" in line]
    assert any("topB: POLY <200>" in line for line in poly_lines)
    assert any("topD: POLY <200>" in line for line in poly_lines)
    assert any("ls1: POLY <202>" in line for line in poly_lines)
    assert any("rs1: POLY <203>" in line for line in poly_lines)


